◊section{
books


◊link["http://www.htdp.org/"]{How to Design Programs
◊img[#:style "height:14rem;margin:0.25rem 0;" #:src "img/htdp-cover.gif"]}
A principled approach to program design. Teaching language support is included with DrRacket. Aimed at the programming novice.


◊link["https://www.realmofracket.com/"]{Realm of Racket
◊img[#:style "height:14rem;margin:0.25rem 0;" #:src "img/racket_cover_web.png"]}
Learn Racket and programming, one game at a time. Sample game code comes with the Racket distribution. For those just starting out with Racket.


◊link["https://cs.brown.edu/~sk/Publications/Books/ProgLangs/2007-04-26/"]{Programming Languages: Application and Interpretation
◊img[#:style "height:14rem;margin:0.25rem 0;" #:src "img/plai-cover.jpg"]}
Foundations of programming languages. Understand the features that make languages tick. For undergraduates, graduate students, and experts.


◊link["https://redex.racket-lang.org/"]{Semantics Engineering 
with PLT Redex
◊img[#:style "height:14rem;margin:0.25rem 0;" #:src "img/redex-cover.jpg"]}
Lightweight automation for semantics. Model your own programming language semantics. For the working language engineer.
